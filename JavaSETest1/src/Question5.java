public class Question5 {
    public static void main(String[] args) {
        int num=15;
        switch(num){
            case 3:
                System.out.println(num +" is equal to 3");
                break;
            case 6:
                System.out.println(num +" is equal to 6");
                break;
            case 9:
                System.out.println(num +" is equal to 9");
                break;
            case 12:
                System.out.println(num +" is equal to 12");
                break;
            case 15:
                System.out.println(num +" is equal to 15");
                break;
            default:
                System.out.println("Error 404");
        }
    }
}
